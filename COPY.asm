CHRTAB  = $1500 ; START OF TABLES
BL0 = $1C00 ; GEORGE
BL1 = $1C00+$558  ; RALPH
BL2 = $1C00+($558*2)  ; LIZZY
BLR0  = $1C00+($558*3)  ; GRORGE REVERSED
BLR1  = $1C00+($558*4)  ; RALPH REVERSED
BLR2  = $1C00+($558*5)  ; LIZZY REVERSED

BUF0  = $CCA0; 48*6 TO STORE
BUF1  = $CCA0+(48*6)
BUF2  = $CCA0+(48*12) ; $33C

APE0  = SO0*64+BANK
APE1  = SO1*64+BANK
APE2  = SO2*64+BANK
  
  ; X=SPRITE NUMBER 0,1,2
PLAYCHAR1 !byte 0,0,0
KEEPX !byte 0

CREATE  
  STX KEEPX
  LDY FRAME,X ; ACTUAL FRAME
  LDA DIR,X
  BPL NORW
  LDA PLAYCHAR1,X
  TAX
  INX
  INX
  INX
  JMP NORW1

NORW
  LDA PLAYCHAR1,X 
  TAX

NORW1
  LDA WHICHT,X    ;,X
  STA WHCR+2  ; HIGHBYTE
  LDA TIM36L,Y  ; Y=FRAME
  CLC
  ADC BLOCKML,X
  STA ALOOP+1
  LDA BLOCKM,X  ; REVERSED
  ADC TIM36H,Y  ; Y=FRAME
  STA ALOOP+2
  LDX KEEPX
  LDA BUFTBL,X  ; X 0 TO 5
  STA SM
  LDA BUFTBH,X  ; X 0 TO 5
  STA SM+1
  LDA #5
  STA LX

BLOOP
  LDA #5
  STA LY
ALOOP
  LDX $FFFF 
  LDA CHRTAB,X
  STA CM+0
WHCR
  LDA CHRTAB+$100,X 
  STA CM+1
  LDY #0
  LDA #255
  LDA CM,Y
  STA SM,Y
  LDY #1
  LDA CM,Y
  LDY #1*6
  STA SM,Y
  LDY #2
  LDA CM,Y
  LDY #2*6
  STA SM,Y
  LDY #3
  LDA CM,Y
  LDY #3*6
  STA SM,Y
  LDY #4
  LDA CM,Y
  LDY #4*6
  STA SM,Y
  LDY #5
  LDA CM,Y
  LDY #5*6
  STA SM,Y
  LDY #6
  LDA CM,Y
  LDY #6*6
  STA SM,Y
  LDY #7
  LDA CM,Y
  LDY #7*6
  STA SM,Y
  INC SM
  BNE SKI1
  INC SM+1
SKI1
  INC ALOOP+1
  BNE SKI2
  INC ALOOP+2
SKI2
  DEC LY
  BPL ALOOP
  LDA SM
  CLC
  ADC #$30-6
  STA SM
  BCC SKI3
  INC SM+1
SKI3
  DEC LX
  ;BPL BLOOP
  BMI *+5
  JMP BLOOP
  RTS

WHICHT
  !word <CHRTAB+$100,<CHRTAB+$200
  !word <CHRTAB+$300,<CHRTAB+$400
  !word <CHRTAB+$500,<CHRTAB+$600

BLOCKM
  !byte <BL0,<BL1,<BL2
  !byte <BLR0,<BLR1,<BLR2

BLOCKML
